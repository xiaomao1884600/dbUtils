    private static final JSONArray MAJOR_SUBTYPE = new JSONArray();

    static {

        //System.out.println(T.substring(3918, 3928));


        for (int i = 0; i < MAJOR_SUB_TYPE_ORIGIN.size(); i ++) {
            JSONArray majorArray = MAJOR_SUB_TYPE_ORIGIN.getJSONArray(i);
            JSONObject majorSubtype = new JSONObject();
            try {
                majorSubtype.put("majorTypeId", majorArray.getString(0));
                majorSubtype.put("majorSubtypeId", majorArray.getString(1));
                majorSubtype.put("majorSubtype", majorArray.getString(2));
            } catch (Exception e) {
                e.printStackTrace();
            }
            MAJOR_SUBTYPE.add(majorSubtype);
        }

    }

    public static String getMajorSubtype(String majorSubtypeId) {
        JSONObject condition = new JSONObject();
        condition.put("majorSubtypeId", majorSubtypeId);
        JSONObject majorSubtypeObject = JSONUtils.findFirst(MAJOR_SUBTYPE, condition);
        if (majorSubtypeObject != null && !majorSubtypeObject.isEmpty()) {
            return majorSubtypeObject.getString("majorSubtype");
        } else {
            return "";
        }
    }

